<script>
    export default{
        data(){
            return {
                IsTopImg:[1,0,0,0,0],
                IsTop:0,
                CTimer:''
            }
        },
        methods:{
            banInter(){
                var i = this.IsTop;
                for(var k = 0;k<5;k++){
                    k==i?
                        this.$set(this.IsTopImg,k,1):
                        this.$set(this.IsTopImg,k,0);
                }
            },
            startBan(){
                this.CTimer = setInterval(()=>{
                    this.IsTop<4?
                        this.IsTop ++:
                        this.IsTop =0;
                    this.banInter();
                },5000);
            },

        },
        mounted(){
            this.startBan();

            //页面rotate效果
            function rot(){
                var els = document.querySelectorAll(".beforeState");
                var win = $(window);
                var $roD = $(".beforeRo");
                win.scroll(function(){
                    for(var el of els){
                         el = $(el);
                         if(el.offset().top+el.height()<=win.scrollTop()+win.height()){
                            el.addClass('afterState');
                         }
                    }  
                    if($roD.offset().top+$roD.height()<=win.scrollTop()+win.height()){
                        $roD.addClass('afterRo');
                    }
                }); 
            }
            rot();
        }
    }
</script>

